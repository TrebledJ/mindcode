package info.teksol.mindcode;

public class MindustryConverterException extends RuntimeException {
    public MindustryConverterException() {
        super();
    }

    public MindustryConverterException(String message) {
        super(message);
    }

    public MindustryConverterException(String message, Throwable cause) {
        super(message, cause);
    }

    public MindustryConverterException(Throwable cause) {
        super(cause);
    }

    protected MindustryConverterException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}
